/* ═══════════════════════════════════════════════════════
   Netra UI — Form Wizard (Vanilla JS)
   Bagian dari Netra UI Design System (modular split)
   Memuat: nt-wizard (multi-step form), step navigation,
           validation, progress, persistence (sessionStorage)
   ═══════════════════════════════════════════════════════

   ── USAGE ──
   <div class="nt-wizard"
        data-style="horizontal | vertical | minimal"
        data-validate="true | false"
        data-allow-step-click="true | false"
        data-allow-skip="false | true"
        data-persist="false | true"
        data-persist-key="my-wizard"
        data-scroll-top="true | false">

     <!-- header / step indicator: auto-generated by JS -->
     <div class="nt-wizard-head"></div>

     <!-- body -->
     <div class="nt-wizard-body">
       <section class="nt-wizard-step" data-step-title="Akun"
                data-step-subtitle="Informasi dasar"
                data-step-icon="fa-solid fa-user">
         ... form fields (.form-group, .form-input, dst) ...
       </section>
       <section class="nt-wizard-step" data-step-title="Profil" ...>
         ...
       </section>
     </div>

     <!-- footer: auto-generated by JS, atau tulis manual dengan
          tombol ber-attribute data-wizard-prev / data-wizard-next /
          data-wizard-submit -->
     <div class="nt-wizard-foot"></div>
   </div>

   ── data-* ATTRIBUTES (pada .nt-wizard) ──
     data-style              "horizontal" (default) | "vertical" | "minimal"
     data-validate           "true" (default) | "false"
                             -> jika true, tombol Next memvalidasi
                             field [required] / [data-required] pada
                             step aktif sebelum pindah step.
     data-allow-step-click   "true" | "false" (default)
                             -> klik langsung pada step header untuk
                             pindah step (hanya ke step yang sudah
                             pernah dilewati / valid).
     data-allow-skip         "true" | "false" (default)
                             -> jika true, step bisa dilewati tanpa
                             validasi sekalipun data-validate=true.
     data-persist            "true" | "false" (default)
                             -> simpan progress step ke sessionStorage
                             sehingga refresh tidak mengulang dari awal.
     data-persist-key        key custom untuk sessionStorage
                             (default: auto dari index wizard di halaman)
     data-scroll-top         "true" (default) | "false"
                             -> auto scroll ke atas wizard saat ganti step
     data-labels-prev        teks tombol Previous (default "Sebelumnya")
     data-labels-next        teks tombol Next (default "Selanjutnya")
     data-labels-submit      teks tombol Submit (default "Simpan")
     data-show-step-count    "true" (default) | "false"
                             -> tampilkan teks "Step 1 dari 4" (style minimal)

   ── PER-STEP data-* (pada .nt-wizard-step) ──
     data-step-title         judul step (wajib, tampil di header)
     data-step-subtitle      deskripsi singkat di bawah judul (opsional)
     data-step-icon          fontawesome class, misal "fa-solid fa-user"
     data-step-optional      "true" -> badge "Opsional", step ini tidak
                             diikutkan dalam validasi wajib

   ── PER-FIELD VALIDATION ──
     Tambahkan attribute `required` atau `data-required="true"` pada
     input/select/textarea. Saat tombol Next ditekan dan
     data-validate="true", field tersebut divalidasi:
       - kosong               -> invalid
       - type=email           -> harus mengandung "@" dan "."
       - pattern attribute    -> divalidasi via regex
     Field invalid akan diberi class `.is-error` dan elemen dengan
     class `.form-msg-error` tepat setelah field akan ditampilkan
     (atau dibuat otomatis jika belum ada & ada data-error-text).

   ── EVENTS (untuk integrasi Laravel / framework lain) ──
     'nt-wizard:init'         { detail: { totalSteps } }
     'nt-wizard:step-change'  { detail: { from, to, step, totalSteps, direction } }
     'nt-wizard:validate'     { detail: { step, valid, invalidFields } }
     'nt-wizard:submit'       { detail: { step, totalSteps, formData } }
                               -> formData adalah object hasil
                               serialize SEMUA input bernama (name=...)
                               di seluruh step wizard.

   ── REVIEW / SUMMARY STEP — AUTO-FILL ──
     Elemen apapun di dalam .nt-wizard dengan attribute
     `data-review-field="nama_field"` akan otomatis diisi dengan nilai
     field bernama `name="nama_field"` dari step lain, dan akan
     diperbarui setiap kali user pindah step. Untuk field
     select/radio/checkbox, teks yang ditampilkan mengikuti label opsi
     terpilih (select: text opsi, radio/checkbox: nilai
     "Ya"/"Tidak" atau value jika tidak ada match khusus).
     Gunakan helper class `.nt-wizard-review-group`,
     `.nt-wizard-review-head`, `.nt-wizard-review-body`,
     `.nt-wizard-review-item` (lihat netra-form-wizard.css) untuk
     menyusun tampilan review yang konsisten dengan design system.

   ── PUBLIC API ──
     window.NetraUI.initWizard(root)
     window.NetraUI.wizardGoto(el, stepIndex)   // el = .nt-wizard element
     window.NetraUI.wizardNext(el)
     window.NetraUI.wizardPrev(el)
   ═══════════════════════════════════════════════════════ */

(function () {

  const DEFAULTS = {
    style: 'horizontal',
    validate: true,
    allowStepClick: false,
    allowSkip: false,
    persist: false,
    scrollTop: true,
    showStepCount: true,
    labelPrev: 'Sebelumnya',
    labelNext: 'Selanjutnya',
    labelSubmit: 'Simpan',
  };

  function bool(val, fallback) {
    if (val === undefined || val === null || val === '') return fallback;
    return val === 'true' || val === true;
  }

  /* ── Serialize semua field bernama di dalam wizard ── */
  function serializeWizard(root) {
    const data = {};
    root.querySelectorAll('[name]').forEach(field => {
      const name = field.name;
      if (!name) return;

      if (field.type === 'checkbox') {
        if (name.endsWith('[]')) {
          if (!data[name]) data[name] = [];
          if (field.checked) data[name].push(field.value);
        } else {
          data[name] = field.checked;
        }
      } else if (field.type === 'radio') {
        if (field.checked) data[name] = field.value;
      } else if (field.tagName === 'SELECT' && field.multiple) {
        data[name] = Array.from(field.selectedOptions).map(o => o.value);
      } else {
        data[name] = field.value;
      }
    });
    return data;
  }

  /* ── Validasi satu field ── */
  function validateField(field) {
    if (field.disabled) return true;

    const required = field.hasAttribute('required') ||
      bool(field.getAttribute('data-required'), false);

    let valid = true;

    if (field.type === 'checkbox' || field.type === 'radio') {
      if (required) {
        const group = field.closest('.nt-wizard-step')
          .querySelectorAll(`[name="${field.name}"]`);
        valid = Array.from(group).some(g => g.checked);
      }
    } else {
      const value = (field.value || '').trim();

      if (required && value === '') {
        valid = false;
      } else if (value !== '') {
        if (field.type === 'email') {
          valid = /^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(value);
        } else if (field.pattern) {
          try { valid = new RegExp(`^(?:${field.pattern})$`).test(value); }
          catch (e) { valid = true; }
        }
      }
    }

    return valid;
  }

  /* ── Set / clear error state visual pada field ── */
  function setFieldError(field, hasError) {
    const cls = field.classList.contains('form-textarea') ? 'form-textarea'
      : field.classList.contains('form-select') ? 'form-select'
        : field.classList.contains('form-checkbox') ? null
          : field.classList.contains('form-radio') ? null
            : 'form-input';

    if (cls) field.classList.toggle('is-error', hasError);

    /* Tampilkan / sembunyikan pesan error otomatis */
    const group = field.closest('.form-group') || field.parentElement;
    if (!group) return;

    let msg = group.querySelector('.nt-wizard-auto-error');
    if (hasError) {
      if (!msg) {
        const text = field.getAttribute('data-error-text') ||
          'Field ini wajib diisi.';
        msg = document.createElement('span');
        msg.className = 'form-msg-error nt-wizard-auto-error';
        msg.innerHTML = `<i class="fa-solid fa-circle-exclamation text-[11px]"></i>${text}`;
        group.appendChild(msg);
      }
    } else if (msg) {
      msg.remove();
    }
  }

  /* ── Validasi seluruh step ── */
  function validateStep(stepEl) {
    const fields = stepEl.querySelectorAll('input, select, textarea');
    const invalidFields = [];

    fields.forEach(field => {
      const ok = validateField(field);
      if (!ok) invalidFields.push(field);

      if (field.type !== 'radio' && field.type !== 'checkbox') {
        setFieldError(field, !ok);
      }
    });

    /* Untuk radio/checkbox required, tandai error pada group wrapper */
    fields.forEach(field => {
      if (field.type === 'radio' || field.type === 'checkbox') {
        const ok = validateField(field);
        const group = field.closest('.form-group');
        if (group) {
          group.classList.toggle('nt-wizard-group-error', !ok);
        }
      }
    });

    return { valid: invalidFields.length === 0, invalidFields };
  }

  /* ── Render header step indicator ── */
  function renderHead(state) {
    const { root, steps, current, opts } = state;
    const head = root.querySelector('.nt-wizard-head');
    if (!head) return;

    if (opts.style === 'minimal') {
      const pct = Math.round(((current + 1) / steps.length) * 100);
      const title = steps[current].dataset.stepTitle || `Step ${current + 1}`;
      const countLabel = bool(opts.showStepCount, true)
        ? `<span class="nt-wizard-min-count">Langkah ${current + 1} dari ${steps.length}</span>`
        : '';

      head.innerHTML = `
        <div class="nt-wizard-min-top">
          <span class="nt-wizard-min-title">${title}</span>
          ${countLabel}
        </div>
        <div class="nt-wizard-progress">
          <div class="nt-wizard-progress-bar" style="width:${pct}%"></div>
        </div>`;
      return;
    }

    if (opts.style === 'vertical') {
      head.innerHTML = `<ol class="nt-wizard-v-list">${
        steps.map((step, i) => renderStepItem(step, i, current, opts, 'vertical')).join('')
      }</ol>`;
      return;
    }

    /* horizontal (default) */
    head.innerHTML = `<ol class="nt-wizard-h-list">${
      steps.map((step, i) => renderStepItem(step, i, current, opts, 'horizontal')).join('')
    }</ol>`;
  }

  function renderStepItem(step, i, current, opts, mode) {
    const title = step.dataset.stepTitle || `Step ${i + 1}`;
    const subtitle = step.dataset.stepSubtitle || '';
    const icon = step.dataset.stepIcon || '';
    const optional = bool(step.dataset.stepOptional, false);

    let state = 'upcoming';
    if (i < current) state = 'done';
    else if (i === current) state = 'active';

    const clickable = bool(opts.allowStepClick, false) &&
      (state !== 'upcoming' || bool(opts.allowSkip, false));

    const iconHtml = state === 'done'
      ? '<i class="fa-solid fa-check"></i>'
      : (icon ? `<i class="${icon}"></i>` : (i + 1));

    const badge = optional ? '<span class="nt-wizard-badge-optional">Opsional</span>' : '';

    if (mode === 'vertical') {
      return `
        <li class="nt-wizard-v-item nt-wizard-step-${state}${clickable ? ' is-clickable' : ''}"
            data-wizard-goto="${i}">
          <span class="nt-wizard-v-marker">${iconHtml}</span>
          <span class="nt-wizard-v-body">
            <span class="nt-wizard-v-title">${title}${badge}</span>
            ${subtitle ? `<span class="nt-wizard-v-sub">${subtitle}</span>` : ''}
          </span>
        </li>`;
    }

    /* horizontal */
    return `
      <li class="nt-wizard-h-item nt-wizard-step-${state}${clickable ? ' is-clickable' : ''}"
          data-wizard-goto="${i}">
        <span class="nt-wizard-h-marker">${iconHtml}</span>
        <span class="nt-wizard-h-label">
          <span class="nt-wizard-h-title">${title}${badge}</span>
          ${subtitle ? `<span class="nt-wizard-h-sub">${subtitle}</span>` : ''}
        </span>
      </li>`;
  }

  /* ── Render footer (jika tidak ditulis manual) ── */
  function renderFoot(state) {
    const { root, steps, current, opts } = state;
    const foot = root.querySelector('.nt-wizard-foot');
    if (!foot) return;
    if (foot.dataset.custom === 'true') return; /* developer sudah isi manual */

    const isFirst = current === 0;
    const isLast = current === steps.length - 1;

    const prevBtn = `<button type="button" class="nt-wizard-btn nt-wizard-btn-prev" data-wizard-prev ${isFirst ? 'disabled' : ''}>
        <i class="fa-solid fa-arrow-left"></i><span>${opts.labelPrev}</span>
      </button>`;

    const nextBtn = `<button type="button" class="nt-wizard-btn nt-wizard-btn-next" data-wizard-next>
        <span>${opts.labelNext}</span><i class="fa-solid fa-arrow-right"></i>
      </button>`;

    const submitBtn = `<button type="submit" class="nt-wizard-btn nt-wizard-btn-submit" data-wizard-submit>
        <span>${opts.labelSubmit}</span><i class="fa-solid fa-check"></i>
      </button>`;

    foot.innerHTML = `
      <div class="nt-wizard-foot-left">${!isFirst ? prevBtn : ''}</div>
      <div class="nt-wizard-foot-right">${isLast ? submitBtn : nextBtn}</div>`;
  }

  /* ── Review step auto-fill ── */
  function updateReviewFields(root) {
    const targets = root.querySelectorAll('[data-review-field]');
    if (!targets.length) return;

    targets.forEach(target => {
      const name = target.getAttribute('data-review-field');
      const fields = root.querySelectorAll(`[name="${name}"]`);
      if (!fields.length) { target.textContent = '—'; return; }

      let display = '';
      const first = fields[0];

      if (first.type === 'checkbox') {
        display = first.checked ? 'Ya' : 'Tidak';
      } else if (first.type === 'radio') {
        const checked = Array.from(fields).find(f => f.checked);
        if (checked) {
          const label = checked.closest('label');
          const labelText = label ? label.querySelector('.form-radio-label, .form-checkbox-label') : null;
          display = labelText ? labelText.textContent.trim() : checked.value;
        }
      } else if (first.tagName === 'SELECT') {
        const opt = first.options[first.selectedIndex];
        display = opt ? opt.textContent.trim() : '';
      } else {
        display = (first.value || '').trim();
      }

      target.textContent = display !== '' ? display : '—';
    });
  }

  /* ── Show step ke-i, sembunyikan lainnya ── */
  function showStep(state, i) {
    state.steps.forEach((step, idx) => {
      step.classList.toggle('nt-wizard-step-hidden', idx !== i);
    });
  }

  /* ── Pindah ke step tertentu ── */
  function gotoStep(state, target, opts2) {
    opts2 = opts2 || {};
    const { root, steps, opts } = state;
    target = Math.max(0, Math.min(steps.length - 1, target));
    if (target === state.current && !opts2.force) return;

    const direction = target > state.current ? 'next' : 'prev';
    const from = state.current;

    state.current = target;
    if (target > state.maxReached) state.maxReached = target;

    showStep(state, target);
    renderHead(state);
    renderFoot(state);
    updateReviewFields(root);

    if (bool(opts.scrollTop, true)) {
      root.scrollIntoView({ behavior: 'smooth', block: 'start' });
    }

    if (bool(opts.persist, false)) {
      try {
        sessionStorage.setItem(state.persistKey, JSON.stringify({
          step: state.current, max: state.maxReached
        }));
      } catch (e) {}
    }

    root.dispatchEvent(new CustomEvent('nt-wizard:step-change', {
      detail: { from, to: target, step: steps[target], totalSteps: steps.length, direction }
    }));
  }

  /* ── Klik Next ── */
  function next(state) {
    const { root, steps, opts, current } = state;
    const stepEl = steps[current];
    const skippable = bool(opts.allowSkip, false) || bool(stepEl.dataset.stepOptional, false);

    if (bool(opts.validate, true) && !skippable) {
      const result = validateStep(stepEl);
      root.dispatchEvent(new CustomEvent('nt-wizard:validate', {
        detail: { step: stepEl, valid: result.valid, invalidFields: result.invalidFields }
      }));
      if (!result.valid) {
        if (result.invalidFields[0]) {
          result.invalidFields[0].focus({ preventScroll: true });
        }
        return;
      }
    }

    if (current === steps.length - 1) {
      submit(state);
      return;
    }
    gotoStep(state, current + 1);
  }

  /* ── Klik Prev ── */
  function prev(state) {
    if (state.current === 0) return;
    gotoStep(state, state.current - 1);
  }

  /* ── Submit akhir ── */
  function submit(state) {
    const { root, steps, opts } = state;

    if (bool(opts.validate, true)) {
      const stepEl = steps[state.current];
      const result = validateStep(stepEl);
      if (!result.valid) {
        if (result.invalidFields[0]) result.invalidFields[0].focus({ preventScroll: true });
        return;
      }
    }

    const formData = serializeWizard(root);

    if (bool(opts.persist, false)) {
      try { sessionStorage.removeItem(state.persistKey); } catch (e) {}
    }

    root.dispatchEvent(new CustomEvent('nt-wizard:submit', {
      detail: { step: state.current, totalSteps: steps.length, formData }
    }));
  }

  /* ── Inisialisasi 1 wizard ── */
  function setupWizard(root, idx) {
    if (root._ntWizard) return;

    const steps = Array.from(root.querySelectorAll('.nt-wizard-step'));
    if (!steps.length) return;

    const opts = {
      style: root.dataset.style || DEFAULTS.style,
      validate: bool(root.dataset.validate, DEFAULTS.validate),
      allowStepClick: bool(root.dataset.allowStepClick, DEFAULTS.allowStepClick),
      allowSkip: bool(root.dataset.allowSkip, DEFAULTS.allowSkip),
      persist: bool(root.dataset.persist, DEFAULTS.persist),
      scrollTop: bool(root.dataset.scrollTop, DEFAULTS.scrollTop),
      showStepCount: bool(root.dataset.showStepCount, DEFAULTS.showStepCount),
      labelPrev: root.dataset.labelsPrev || DEFAULTS.labelPrev,
      labelNext: root.dataset.labelsNext || DEFAULTS.labelNext,
      labelSubmit: root.dataset.labelsSubmit || DEFAULTS.labelSubmit,
    };

    root.classList.add(`nt-wizard-style-${opts.style}`);

    const persistKey = root.dataset.persistKey || `nt-wizard-${idx}`;

    let startStep = 0;
    let maxReached = 0;
    if (bool(opts.persist, false)) {
      try {
        const saved = JSON.parse(sessionStorage.getItem(persistKey) || 'null');
        if (saved && typeof saved.step === 'number') {
          startStep = Math.min(saved.step, steps.length - 1);
          maxReached = Math.min(saved.max || 0, steps.length - 1);
        }
      } catch (e) {}
    }

    const state = { root, steps, opts, current: startStep, maxReached, persistKey };
    root._ntWizard = state;

    /* Pastikan ada head & foot container */
    if (!root.querySelector('.nt-wizard-head')) {
      const head = document.createElement('div');
      head.className = 'nt-wizard-head';
      root.insertBefore(head, root.firstChild);
    }
    if (!root.querySelector('.nt-wizard-body')) {
      const body = document.createElement('div');
      body.className = 'nt-wizard-body';
      steps.forEach(s => body.appendChild(s));
      root.appendChild(body);
    }
    if (!root.querySelector('.nt-wizard-foot')) {
      const foot = document.createElement('div');
      foot.className = 'nt-wizard-foot';
      root.appendChild(foot);
    }

    showStep(state, state.current);
    renderHead(state);
    renderFoot(state);
    updateReviewFields(root);

    /* ── Event delegation ── */
    root.addEventListener('click', e => {
      const nextBtn = e.target.closest('[data-wizard-next]');
      if (nextBtn) { e.preventDefault(); next(state); return; }

      const prevBtn = e.target.closest('[data-wizard-prev]');
      if (prevBtn) { e.preventDefault(); prev(state); return; }

      const submitBtn = e.target.closest('[data-wizard-submit]');
      if (submitBtn) {
        /* Jika di dalam <form>, biarkan native submit terjadi setelah
           validasi sukses; jika bukan form, panggil submit manual. */
        if (!root.closest('form')) { e.preventDefault(); submit(state); }
        else {
          if (bool(opts.validate, true)) {
            const result = validateStep(steps[state.current]);
            if (!result.valid) {
              e.preventDefault();
              if (result.invalidFields[0]) result.invalidFields[0].focus({ preventScroll: true });
              return;
            }
          }
          const formData = serializeWizard(root);
          root.dispatchEvent(new CustomEvent('nt-wizard:submit', {
            detail: { step: state.current, totalSteps: steps.length, formData }
          }));
        }
        return;
      }

      const gotoEl = e.target.closest('[data-wizard-goto]');
      if (gotoEl) {
        const target = parseInt(gotoEl.dataset.wizardGoto, 10);
        const clickable = bool(opts.allowStepClick, false) &&
          (target <= state.maxReached || bool(opts.allowSkip, false));
        if (clickable) gotoStep(state, target);
      }
    });

    /* ── Clear error saat user mengisi ulang field ── */
    root.addEventListener('input', e => {
      const field = e.target;
      if (field.matches('input, select, textarea')) {
        if (validateField(field)) setFieldError(field, false);
      }
    });
    root.addEventListener('change', e => {
      const field = e.target;
      if (field.matches('input[type="checkbox"], input[type="radio"], select')) {
        if (validateField(field)) {
          const group = field.closest('.form-group');
          if (group) group.classList.remove('nt-wizard-group-error');
        }
      }
    });

    root.dispatchEvent(new CustomEvent('nt-wizard:init', {
      detail: { totalSteps: steps.length }
    }));
  }

  function initWizard(rootDoc) {
    rootDoc = rootDoc || document;
    rootDoc.querySelectorAll('.nt-wizard').forEach((el, idx) => setupWizard(el, idx));
  }

  /* ── Public goto/next/prev (untuk dipanggil dari luar) ── */
  function wizardGoto(el, stepIndex) {
    if (el && el._ntWizard) gotoStep(el._ntWizard, stepIndex, { force: true });
  }
  function wizardNext(el) {
    if (el && el._ntWizard) next(el._ntWizard);
  }
  function wizardPrev(el) {
    if (el && el._ntWizard) prev(el._ntWizard);
  }

  document.addEventListener('DOMContentLoaded', () => initWizard(document));

  if (!window.NetraUI) window.NetraUI = {};
  Object.assign(window.NetraUI, {
    initWizard,
    wizardGoto,
    wizardNext,
    wizardPrev,
  });

})();
